"""Day 2: Mathematical operations in Python."""

# Addition
print(5 + 3)

# Subtraction
print(10 - 4)

# Multiplication
print(3 * 4)

# Division always returns a float.
print(12 / 3)

# Exponents
print(2 ** 3)

# Python follows PEMDAS:
# Parentheses, Exponents, Multiplication/Division, Addition/Subtraction.
result = 3 * (3 + 3) / 3 - 3
print(result)

# The modulo operator returns the remainder.
remainder = 10 % 3
print(remainder)
