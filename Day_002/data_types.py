"""Day 2: Python data types."""

# String
name = "Raza"
print(name)
print(name[0])
print("Hello"[4])

# Integer
age = 25
print(age)

# Underscores make large numbers easier to read.
population = 8_000_000_000
print(population)

# Float
height = 5.9
print(height)

# Boolean
is_learning_python = True
has_finished_course = False
print(is_learning_python)
print(has_finished_course)

# type() shows the data type of a value.
print(type(name))
print(type(age))
print(type(height))
print(type(is_learning_python))
